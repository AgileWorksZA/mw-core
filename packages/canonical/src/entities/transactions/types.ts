/**
 * MoneyWorks Transaction filter for search/query operations
 *
 * @ai-instruction Use for querying transactions
 */
export interface MoneyWorksTransactionFilter {
	/**
	 * Filter by transaction type
	 */
	type?: MoneyWorksTransactionType | string;

	/**
	 * Filter by status
	 */
	status?: MoneyWorksTransactionStatus | string;

	/**
	 * Filter by customer/supplier code
	 */
	nameCode?: string;

	/**
	 * Filter by period
	 */
	period?: number;

	/**
	 * Filter by date range - start
	 */
	fromDate?: string;

	/**
	 * Filter by date range - end
	 */
	toDate?: string;

	/**
	 * Filter by our reference
	 */
	ourRef?: string;

	/**
	 * Filter by their reference
	 */
	theirRef?: string;

	/**
	 * Search text (searches Description, OurRef, TheirRef)
	 */
	searchText?: string;
}
